package com.example.helper;

public class MethodUtils {

    public static Integer m1(Integer x, Integer y) {
        return x + 2 * y;
    }

    public static Integer m2(Integer x, Integer y) {
        return 2 * x + y;
    }

    public static Integer m3(Integer m1, Integer m2) {
        return 2 * m1 + 2 * m2;
    }

}
